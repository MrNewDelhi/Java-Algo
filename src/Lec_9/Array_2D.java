package Lec_9;

public class Array_2D {
    public static void main(String[] args) {
        int rows = 3;
        int columns = 5;
        int[][] arr = new int[rows][columns];
        System.out.println(arr);
        System.out.println(arr.length);
        System.out.println(arr[0].length);
        System.out.println(arr[1].length);
    }
}
